# number datatypes

# Integer
integer_number = int(input("Enter a integer: "))
print("Integer Number:", integer_number)
print("Type of integer_number:", type(integer_number))

# Float
float_number = float(input("Enter a number with decimal points: "))
print("Float Number:", float_number)
print("Type of float_number:", type(float_number))

# Complex
input_str = input("Enter a real and imaginary number: ")
complex_number = complex(input_str)
print("Complex Number:", complex_number)
print("Type of complex_number:", type(complex_number))
