# arithmetic operations

a = int(input("Enter a: "))
b = int(input("Enter b: "))

print(a, "+", b, "=", a + b)

print(a, "-", b, "=", a - b)

print(a, "*", b, "=", a * b)

print(a, "/", b, "=", a / b)

print(a, "//", b, "=", a // b)

print(a, "%", b, "=", a % b)

print(a, "**", b, "=", a ** b)
