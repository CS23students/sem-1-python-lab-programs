# tuple operations

tuple_1 = (1, 2, 3, 4, 5)
tuple_2 = (9, 7, 8, 6)

# create
print("creating :", tuple_1)


# concatenate
print("concatenating :", tuple_1+tuple_2)

# length
print("Length of tuple_1: ", len(tuple_1))

# maximum
print("Maximum of tuple_1: ", max(tuple_1))

# minimum
print("Minimum of tuple_1: ", min(tuple_1))

# sort
print("Sorted tuple_1: ", sorted(tuple_2))
