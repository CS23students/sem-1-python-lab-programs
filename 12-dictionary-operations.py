# dictionary operations

dict_1 = {1: "apple", 2: "banana"}

# create
print("creating :", dict_1)

# accessing
val = dict_1[1]
print("accessing: ", val)

# update
dict_1[1] = 'orange'
print("Updated value : ", dict_1)

# added value
dict_1[3] = 'grapes'
print("Added value : ", dict_1)

# delete
del dict_1[2]
print("Delete: ", dict_1)
